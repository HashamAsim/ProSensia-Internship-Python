def analyze_numbers(num_list):
    for i in range(len(num_list)):
        for j in range(0, len(num_list) - i - 1):
            if num_list[j] > num_list[j + 1]:
                num_list[j], num_list[j + 1] = num_list[j + 1], num_list[j]

    total = 0
    minimum = num_list[0]
    maximum = num_list[0]

    for num in num_list:
        total += num
        if num < minimum:
            minimum = num
        if num > maximum:
            maximum = num

    avg = total / len(num_list)

    result = {
        "sorted": num_list,
        "sum": total,
        "average": avg,
        "min": minimum,
        "max": maximum
    }

    return result


